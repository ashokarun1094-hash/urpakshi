// Editable Pancha Pakshi configuration — per-weekday 5×5 activity grids.
// Every cell is explicit so you can change any bird×slot activity independently.
// Rows = birds (in `birds` order). Columns = main-slot index 0..4 (0 = first slot
// after sunrise for day / after sunset for night; each slot = 144 min).
// subDur = minutes for each activity within a 144-min slot (must total 144).

import type { Bird, Activity } from "./pakshi";

export interface WeekdayConfig {
  /** grid[birdRow][mainSlotCol] = activity for that cell. */
  grid: Activity[][];
  /** Minutes per activity inside a 144-min main slot. Must sum to 144. */
  subDur: Record<Activity, number>;
}

export interface PakshaFullConfig {
  /** Bird row order used by grid[row]. */
  birds: Bird[];
  /** 7 weekdays: 0=ஞாயிறு .. 6=சனி */
  weekdays: WeekdayConfig[];
}

